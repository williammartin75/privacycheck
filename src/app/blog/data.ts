export function generateBlogMetadata(post: BlogPost): Metadata {
    return {
        title: `${post.title} | PrivacyChecker Blog`,
        description: post.description,
        keywords: post.keywords.join(', '),
        openGraph: {
            title: post.title,
            description: post.description,
            type: 'article',
            publishedTime: post.date,
            url: `https://privacychecker.pro/blog/${post.slug}`,
            siteName: 'PrivacyChecker',
        },
        twitter: {
            card: 'summary_large_image',
            title: post.title,
            description: post.description,
        },
        alternates: {
            canonical: `https://privacychecker.pro/blog/${post.slug}`,
        },
    };
}
